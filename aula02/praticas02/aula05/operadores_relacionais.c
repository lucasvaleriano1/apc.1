#include <stdio.h>

int main() {
    //< menor que
    //<= menor igual
    //> maior
    //>= maior igual
    // == igual
    // != diferente

    printf("2 eh maior que 5? %i\n", 2 > 5);
    printf("2 eh maior igual que 5? %i\n", 2 >= 5);
    printf("2 eh menor que 5? %i\n", 2 < 5);
    printf("2 eh menor igual que 5? %i\n", 2 <= 5);
    printf("2 eh igual que 5? %i\n", 2 == 5);
    printf("2 eh diferente que 5? %i\n", 2 != 5);

    return 0;

}